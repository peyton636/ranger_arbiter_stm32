/* PHY BMSR bit2: 1=link up */
u8 LAN8720_LinkUp(void)
{
	u16 bmsr;

	bmsr = ETH_ReadPHYRegister(LAN8720_PHY_ADDRESS, 1);
	return (bmsr & 0x0004u) ? 1u : 0u;
}

void ETH_RecoverRxDma(void)
{
	if((ETH->DMASR & ETH_DMASR_RBUS) != (u32)RESET)
	{
		ETH->DMASR = ETH_DMASR_RBUS;
		ETH->DMARPDR = 0;
	}
}

u8 ETH_RxDescCpuCount(void)
{
	u8 i, cnt = 0;
	ETH_DMADESCTypeDef *d = DMARxDscrTab;

	if(!d)
		return 0;
	for(i = 0; i < ETH_RXBUFNB; i++)
	{
		if((d->Status & ETH_DMARxDesc_OWN) == (u32)RESET)
			cnt++;
		d = (ETH_DMADESCTypeDef *)(d->Buffer2NextDescAddr);
	}
	return cnt;
}

u8 ETH_DmaSrHasRbus(void)
{
	return ((ETH->DMASR & ETH_DMASR_RBUS) != (u32)RESET) ? 1u : 0u;
}

u32 ETH_TxOkCount(void)
{
	return s_eth_tx_ok;
}

