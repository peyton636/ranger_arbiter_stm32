/* 调度器未启动：delay_nms 忙等；已启动：vTaskDelay 让出 CPU。
 * 启动阶段（Hardware_Check / main 外设 Init）应只在调度器前调用。 */
void delay_ms(u16 nms)
{
	u8 repeat;
	u16 remain;

	if(xTaskGetSchedulerState() != taskSCHEDULER_NOT_STARTED)
	{
		vTaskDelay(pdMS_TO_TICKS(nms));
		return;
	}

	repeat=nms/540;
	remain=nms%540;
	while(repeat)
	{
		delay_nms(540);
		repeat--;
	}
	if(remain)delay_nms(remain);
} 


